print("Square and Cube of numbers from 50 to 100 are")
      
counter=50

while(counter>=50 and counter <=100):
    print("\n",counter)
    square=counter*counter
    cube=counter*counter*counter
    print("The square of",counter,"is", square)
    print("The cube of",counter,"is\n", cube)
    counter=counter + 1
